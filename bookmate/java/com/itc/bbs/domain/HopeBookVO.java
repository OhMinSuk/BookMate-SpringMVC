package com.itc.bbs.domain;

public class HopeBookVO {
	private int hopeNo;
	private int userNo;
	private String hopeName;
	private String hopeISBN;
	
	public int getHopeNo() {
		return hopeNo;
	}
	public void setHopeNo(int hopeNo) {
		this.hopeNo = hopeNo;
	}
	public int getUserNo() {
		return userNo;
	}
	public void setUserNo(int userNo) {
		this.userNo = userNo;
	}
	public String getHopeName() {
		return hopeName;
	}
	public void setHopeName(String hopeName) {
		this.hopeName = hopeName;
	}
	public String getHopeISBN() {
		return hopeISBN;
	}
	public void setHopeISBN(String hopeISBN) {
		this.hopeISBN = hopeISBN;
	}
	
	
}
